const searchName = async (db, event) => {
  console.info(RESOURCE_NAME + ' searchName()');
  // let multivalue = event.multiValueQueryStringParameters
// if(multivalue.length>0){
//   console.log('multivaluequerystrings:' +multivalue)
// }
  let contentName = (event.queryStringParameters 
                      && 'contentName' in event.queryStringParameters
                      && typeof event.queryStringParameters.contentName !== 'undefined') ? 
                          event.queryStringParameters.contentName : null;

  let criteria = { };
  if (contentName) {
    criteria["name LIKE"] = `%${contentName}%`; 
  }
  else {
    criteria["name IS NOT"] = null; 
  }

  let searchResult = await db.cms_content.find(criteria);
  return searchResult;
}

